package first1;

public class MathClassMethods25 {
	public static void main (String args[]) {
		System.out.println("Absolute Value of -20 is: " + Math.abs(-20));
	    System.out.println("Ceiling of 9.2 is: " + Math.ceil(9.2));
	    System.out.println("Ceiling of -9.2 is: " + Math.ceil(-9.2));
	    System.out.println("Floor of 9.8 is: " + Math.floor(9.8));
	    System.out.println("Floor of -9.8 is: " + Math.floor(-9.8));
	    System.out.println("Max between 5.3 & 7.9 is: " + Math.max(5.3,7.9));
	    System.out.println("Min between 5.3 & 7.9 is: " + Math.min(5.3,7.9));
	    System.out.println("5 raised to the power of 3 is: " + Math.pow(5,3));
	    System.out.println("The sqrt of 9 is: " + Math.sqrt(9));
	}
}
